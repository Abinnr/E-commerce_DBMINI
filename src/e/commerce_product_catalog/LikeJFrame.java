    private void LikeData(){
            
        try {
            int QQ;
            pst = con.prepareStatement("SELECT * FROM likeproduct");
            ResultSet Rs = pst.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();

            QQ = RSMD.getColumnCount();
            
            DefaultTableModel DFG =(DefaultTableModel)liketable.getModel(); 
            
            DFG.setRowCount(0);
             
            while(Rs.next()){
        
            Vector v2 = new Vector();
             
            for(int aa=1; aa<=QQ; aa++){
                v2.add(Rs.getString("likeid")); 
                v2.add(Rs.getString("productid"));
                v2.add(Rs.getString("productname"));
                //v2.add(Rs.getString("count"));
                v2.add(Rs.getString("productprice"));
                //v2.add(Rs.getString("address"));
             }
             
             DFG.addRow(v2);

        }
        }catch (SQLException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
